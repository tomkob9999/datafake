const convKanjiToKunyomi = (kanji) => {
    // console.log("kanji", kanji)
    let len = kanji.length;
    let retStr = "";
    for (let i=0; i<len; i++) {

        if (kanji[i] in kanji_dict) {
            // console.log("kanji[i]", kanji[i])
            let numKun = kanji_dict[kanji[i]].kunyomi.length;
            // console.log("numKun", numKun)
            if (numKun > 0) {
                // console.log("kanji_dict[kanji[i]].kunyomi[0]", kanji_dict[kanji[i]].kunyomi[0])
                retStr += kanji_dict[kanji[i]].kunyomi[0];
            }
            else {
                let numOn = kanji_dict[kanji[i]].onyomi.length;
                if (numOn > 0) {
                    // console.log("kanji_dict[kanji[i]].onyomi[0]", kanji_dict[kanji[i]].onyomi[0])
                    const kata = kanji_dict[kanji[i]].onyomi[0];
                    for (let j=0; j<kata.length; j++) {
                        retStr += kata_to_hira[kata[j]];
                    }
                }
            }
        } else {
            retStr += "わか"
        }
    }
    // console.log("retStr", retStr)
    return retStr;
}

